u_int16_t IIS2_GET_DATA(uint8_t addr, uint16_t dataSize){
	uint16_t val = 0;
	uint16_t value = 0;
	uint8_t receiveData[dataSize];
	HAL_I2C_Mem_Read(&hi2c1, IIS2_ADDR, addr, 1, receiveData, dataSize, 100);
	value = (receiveData[1] << 8 | receiveData[0]);
	if(value > 0x7fff){
		val = ~value;
	}
	else{
		val = value;
	}
	return val;
}
