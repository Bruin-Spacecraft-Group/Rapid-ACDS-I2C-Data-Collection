void UART_PRINT_VAL(double value){
    char total[50];
    char temp[10];
    if(value < 0){
       UART_PRINT_TEXT("-");
    }
    sprintf(total, "%i", abs((int)value));
    strcat(total, ".");
    double currentVal = (value - (int) value);
    for(int a=0;a<6;a++){
        currentVal *= 10;
        sprintf(temp, "%i", abs((int)currentVal));
        strcat(total, temp);
        currentVal -= (int)currentVal;
    }
    HAL_UART_Transmit(&huart2, total, strlen(total), 100);
}
